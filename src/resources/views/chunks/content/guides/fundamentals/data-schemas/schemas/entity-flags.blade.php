<h2 id="sec-shared-item-structure">Entity flags schema</h2>
<p>Contains a list of various boolean values regarding the state of an entity.</p>

<h3 class="text-monospace mt-5" id="sec-item/tag">is_on_fire</h3>
<hr>

<p>The <code>is_on_fire</code> boolean states whether or not the entity is on fire.</p>

<pre class="pre-scrollable bg-dark text-light"><code class="json px-4 py-3">"entity_flags_object": {
    "is_on_fire": true
}</code></pre>

